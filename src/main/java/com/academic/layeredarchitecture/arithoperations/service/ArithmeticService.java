package main.java.com.academic.layeredarchitecture.arithoperations.service;

public class ArithmeticService {
	
	
	public float add(float valOne, float valueTwo) {
		return valOne + valueTwo;
	}
	
	public float substract(float valOne, float valueTwo) {
		return valOne - valueTwo;
	}
	
	public float multiply(float valOne, float valueTwo) {
		return valOne * valueTwo;
	}
	
	public float divide(float valOne, float valueTwo) {
		return valOne + valueTwo;
	}

}
